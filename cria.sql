CREATE TABLE USUARIOS (
    ID_USUARIO  INT             PRIMARY KEY NOT NULL,
    NOME        VARCHAR2(100)   NOT NULL,
    EMAIL       VARCHAR2(100)   NOT NULL,
    PRIORIDADE  INT             CHECK (PRIORIDADE IN (1, 2, 3)),
    SALDO       NUMERIC(10,2)
);

CREATE TABLE INSCRICOES (
    ID_INSCRICAO    INT             PRIMARY KEY NOT NULL,
    ID_USUARIO      INTEGER         REFERENCES USUARIOS (ID_USUARIO),
    STATUS          VARCHAR2(10)    CHECK (STATUS IN ('ATIVO','INATIVO')),
    VALOR_PG        NUMERIC(10,2),
    TIPO            VARCHAR2(100),
    DATA_INSCRICAO  DATE,
    WAITLIST        VARCHAR2(3)     CHECK (WAITLIST IN ('SIM','NAO'))
);

CREATE TABLE LOG_AUDITORIA (
    ID_LOG          INT             PRIMARY KEY NOT NULL,
    ID_INSCRICAO    INTEGER         REFERENCES INSCRICOES (ID_INSCRICAO),
    MOTIVO          VARCHAR2(60),
    LOG_DATA        DATE
);

CREATE TABLE HISTORICO_STATUS (
    ID_HISTORICO    INTEGER         PRIMARY KEY NOT NULL,
    ID_INSCRICAO    INTEGER         REFERENCES INSCRICOES (ID_INSCRICAO),
    STATUS_ANTERIOR VARCHAR2(10),
    STATUS_NOVO     VARCHAR2(10),
    DT_ALTERACAO    DATE
);

CREATE SEQUENCE SEQ_HISTORICO_STATUS
START WITH 1
INCREMENT BY 1;

